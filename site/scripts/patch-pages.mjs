/**
 * Postbuild patch for Cloudflare Pages compatibility.
 *
 * @astrojs/cloudflare v13 generates dist/server/wrangler.json with fields
 * that Pages rejects (main, pages_build_output_dir, assets, kv_namespaces,
 * rules, no_bundle, triggers, and dozens of undocumented keys).
 *
 * It also places the Worker entry at dist/server/entry.mjs instead of the
 * dist/_worker.js location that Pages auto-detects.
 *
 * This script:
 * 1. Creates dist/_worker.js — re-exports the Astro Worker from server/entry.mjs
 * 2. Deletes dist/server/wrangler.json so Wrangler does not redirect its config
 *    away from site/wrangler.toml, which would cause static assets to be skipped.
 * 3. Deletes .wrangler/deploy/config.json if it caches that redirect path, since
 *    Wrangler errors out when the cached redirect target no longer exists.
 * 4. Copies public/ files to dist/ root so static assets are served at correct URL
 *    paths by env.ASSETS.fetch() (e.g. /logo.jpg, /favicon.svg).
 */

import fs from 'node:fs';

const siteDir = new URL('../', import.meta.url).pathname;
const distDir = siteDir + 'dist/';
const serverWrangler = distDir + 'server/wrangler.json';
const workerShim = distDir + '_worker.js';
const deployConfig = siteDir + '.wrangler/deploy/config.json';

// ── 1. Create _worker.js shim ─────────────────────────────────────────────────

fs.writeFileSync(
  workerShim,
  `// Auto-generated by scripts/patch-pages.mjs
// Cloudflare Pages auto-detects _worker.js as the SSR Worker entry.
export { default } from './server/entry.mjs';
`,
);
console.log('Created dist/_worker.js');

// ── 2. Delete wrangler.json to prevent Wrangler config redirect ───────────────

if (!fs.existsSync(serverWrangler)) {
  console.log('dist/server/wrangler.json not found — nothing to delete');
  process.exit(0);
}

fs.unlinkSync(serverWrangler);
console.log('Deleted dist/server/wrangler.json to prevent Wrangler config redirect');

// ── 3. Delete .wrangler/deploy/config.json if it points to the now-gone file ──

if (fs.existsSync(deployConfig)) {
  fs.unlinkSync(deployConfig);
  console.log('Deleted .wrangler/deploy/config.json (cached redirect — no longer valid)');
}

// ── 4. Copy public/ files to dist/ root for correct ASSETS URL mapping ────────
const publicDir = new URL('../public/', import.meta.url).pathname;
fs.cpSync(publicDir, distDir, { recursive: true, force: true });
console.log('Copied public/ files to dist/ root for correct ASSETS URL mapping');
